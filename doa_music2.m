%% DOA MUSIC
%% 初始化参数 initial parameter
close all;clear all;clc;
source=2;       %信源  signal number 期望信号
sensor=7;            %array number
theta=[20 60];      %DOA of signal
ss=1024;        %snapshot  快拍数
snr=[40 60];    %  SNR  信噪比
j=sqrt(-1);
%% 信号复包络 SIGNAL
w=[pi/6 pi/5]';
for m=1:source
    S(m,:)=10.^(snr(m)/10)*exp(-j*w(m)*[0:ss-1]);
end
%% 阵列流形  STEERING VECTOR
A=exp(-j*(0:sensor-1)'*pi*sin(theta/180*pi));
%% 噪声  NOISE
N=randn(sensor,ss)+j*randn(sensor,ss);
%% 观测信号  SIGNAL RECEIVED
Y=A*S+N;
%% 阵列协方差矩阵  COVIARIANCE MATRIX
R=Y*Y'/ss;
%% 特征分解 eigen-decomposition
p=length(theta);
[E,X,V]=svd(R);%E=V,        X是特征值，E是特征向量。见张贤达 现代信号处理 P131 3.9.22式
%Es=X(:,1:p); 
En=E(:,p+1:sensor);%X特征值由大到小 降序排列 所以大特征值在前 即信号特征值在前

% [V,D]=eig(R);%[特征向量，特征值]
% D=diag(D);
% Es=V(:,sensor-p+1:sensor);
% En=V(:,1:sensor-p);%噪声特征向量  特征值由小到大 升序排列 所以大特征值在后 即信号特征值在后

search_doa=[0:1:90];
for i=1:length(search_doa)
    a_theta=exp(-j*pi*(0:sensor-1)'*sin(search_doa(i)*pi/180));
    Q=(a_theta)'*En*En'*a_theta;
    Pmusic(i)=1./Q;
end
P_music=10*log10(Pmusic);
plot(search_doa,P_music,'linewidth',2);
title('MUSIC beamforming');
xlabel('angle/degree');
ylabel('magnitude/dB');
grid;
